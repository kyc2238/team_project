package com.care.root.professor.dto;

public class AccumInfoDTO {
	private int applyGrade;
	private int receivedGrade;
	private double aveGrade;
	private double totalScore;
	
	public int getApplyGrade() {
		return applyGrade;
	}
	public void setApplyGrade(int applyGrade) {
		this.applyGrade = applyGrade;
	}
	public int getReceivedGrade() {
		return receivedGrade;
	}
	public void setReceivedGrade(int receivedGrade) {
		this.receivedGrade = receivedGrade;
	}
	public double getAveGrade() {
		return aveGrade;
	}
	public void setAveGrade(double aveGrade) {
		this.aveGrade = aveGrade;
	}
	public double getTotalScore() {
		return totalScore;
	}
	public void setTotalScore(double totalScore) {
		this.totalScore = totalScore;
	}
	
	
}
